#include "libft.h"

void		*ft_memset(void *b, int c, size_t len)
{
	size_t	i;
	char	*a;

	if (len == 0)
		return (b);
	a = (char *)b;
	i = 0;
	while (i <= len - 1)
	{
		a[i] = c;
		i++;
	}
	return (a);
}
